#!/bin/sh

set -eu
name=${1:?slide名を引数に実行してください}

mkdir $name

LINK="https://vividmuimui.github.io/slides/$name/slides"
if ! grep -e "$LINK" README.md > /dev/null
then
    echo "- [$name]($LINK)" >> README.md
fi

cd $name

cp ../config.yml ./
cat << EOS >> config.yml
title: authentication
meta_properties:
  og:type: "website"
  og:title: $name
  og:description: Slide about '$name'. Generated by reveal-ck.
  og:url: "https://vividmuimui.github.io/slides/$name/slides"

meta_names:
  twitter:card: "summary"
  twitter:site: "@vivid_muimui"
  twitter:creator: "@vivid_muimui"
  twitter:title: $name
  twitter:description: Slide about '$name'. Generated by reveal-ck.
EOS

mkdir slides
touch slide.md
reveal-ck generate -f slide.md -d slides
cat ../css/* >> ./slides/css/reveal.css
